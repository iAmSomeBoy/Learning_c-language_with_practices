#include <stdio.h>

int main(void) {
    int i = 10, i1;
    float f = 8.5, f1;

    i1 = f;
    printf("i1 = %d\n", i1);

    f1 = i;
    printf("f1 = %f\n", f1);

    i1 = 5 / 2;
    printf("i1 = 5 / 2 = %d\n", i1);

    i1 = 5.0 / 2;
    printf("i1 = 5.0 / 2 = %d\n", i1);

    f1 = 5 / 2;
    printf("f1 = 5 / 2 = %f\n", f1);

    f1 = 5.0 / 2;
    printf("f1 = 5.0 / 2 = %f\n", f1);

    f1 = (float) 5 / 2;
    printf("f1 = %f\n", f1);

    return 0;
}  


















